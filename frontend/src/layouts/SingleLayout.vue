<template>
  <q-layout view="lHh Lpr lFf">
    <q-header elevated>
      <q-toolbar>
        <q-btn flat dense round icon="menu" aria-label="Menu" />

        <q-toolbar-title> Mappa Frontend </q-toolbar-title>

        <div :title="proxyHCTitle">Proxy: {{ proxyHC }}</div>
        <q-icon :name="proxyIcon" />
      </q-toolbar>
    </q-header>

    <q-page-container>
      <router-view />
    </q-page-container>
  </q-layout>
</template>

<script lang="ts">
import { defineComponent } from 'vue';
import { DoHC } from 'src/services/mappa';
export default defineComponent({
  name: 'SingleLayout',

  components: {},
  data() {
    return {
      proxyHC: '...',
      proxyHCTitle: '',
      proxyIcon: 'link',
    };
  },

  async mounted() {
    console.log('Mounting single layout');
    try {
      let hc = await DoHC();
      console.log('HC', hc);
      this.proxyHCTitle = `Server: ${hc.mappa_server.status}`;
      this.proxyHC = hc.status == 'HEALTHY' ? 'OK' : 'Indispon√≠vel';
      this.proxyIcon = this.proxyHC == 'OK' ? 'link' : 'link_off';
    } catch (err) {
      console.error('HC', err);
      this.proxyHCTitle = (err as Error).message;
      this.proxyHC = 'Em erro';
      this.proxyIcon = 'link_off';
    }
    console.log('Mounted single layout');
  },
});
</script>
